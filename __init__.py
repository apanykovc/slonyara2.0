"""Пакет бота‑напоминалки для встреч в Telegram.

Содержит лёгкую обёртку ``main``, которая импортирует тяжёлые зависимости
только при запуске. Это позволяет импортировать пакет без предварительной
установки библиотек исполнения (например, ``pytz`` или ``python-telegram-bot``).
"""

from importlib import import_module

__all__ = ["main"]


def main(*args, **kwargs):
    """Запустить бота напоминаний.

    Реализация находится в :mod:`telegram_meeting_bot.bot.main` и импортируется
    лениво, чтобы избежать обязательных тяжёлых импортов при загрузке пакета.
    Все позиционные и именованные аргументы пробрасываются в базовую функцию
    ``main``.
    """

    return import_module(".bot.main", __name__).main(*args, **kwargs)

